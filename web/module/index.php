			<h2 class="featurette-heading">Magic, Tech. <span class="text-muted">Naturally.</span></h2>
			<p class="lead">Botania is a tech mod themed around natural magic. Gameplay is centered around creating magical flowers and devices using <i>Mana</i>, the power of the earth.<br><br>
			
			Botania is fully playable as a standalone mod (and is designed as such), but it functions just well in conjunction with other mods.<br><br>

			The mod focuses on automation, but without elements that make it feel grindy-- systems can be thought out and designed, rather than built blindly from blueprints in JEI or a wiki.<br><br>
			
			<b>Botania's design philosophy:</b><br>
			<span class="glyphicon glyphicon-ok"></span> No pipes, wires, etc-- they're boring and overdone;<br>
			<span class="glyphicon glyphicon-ok"></span> No GUIs-- interact with everything in-world;<br>
			<span class="glyphicon glyphicon-ok"></span> No numbers-- players shouldn't try to minmax everything;<br>
			<span class="glyphicon glyphicon-ok"></span> Keep all resources as renewable as possible;<br>
			<span class="glyphicon glyphicon-ok"></span> Provide pleasing visuals, despite using only two different particle effects.<br><br>
			<b>Botania's goals:</b><br>
			<span class="glyphicon glyphicon-ok"></span> Break the mold of mods being means to ends-- be engaging in gameplay as well as in rewards;<br>
			<span class="glyphicon glyphicon-ok"></span> Deliver fun and balanced gameplay without forcing players to grind;<br>
			<span class="glyphicon glyphicon-ok"></span> Incentivize sandbox gameplay and allow players to create fun contraptions;<br>
			<span class="glyphicon glyphicon-ok"></span> Create an overall immersive experience.<br>
